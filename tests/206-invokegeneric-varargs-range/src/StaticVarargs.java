import java.util.Arrays;

public class StaticVarargs {
    public static String toString(boolean... elements) {
      return Arrays.toString(elements);
    }

    public static String toString(byte... elements) {
      return Arrays.toString(elements);
    }

    public static String toString(char... elements) {
      return Arrays.toString(elements);
    }

    public static String toString(short... elements) {
      return Arrays.toString(elements);
    }

    public static String toString(int... elements) {
      return Arrays.toString(elements);
    }

    public static String toString(long... elements) {
      return Arrays.toString(elements);
    }

    public static String toString(float... elements) {
      return Arrays.toString(elements);
    }

    public static String toString(double... elements) {
      return Arrays.toString(elements);
    }

    public static String toString(Object... elements) {
      return Arrays.toString(elements);
    }
  }
